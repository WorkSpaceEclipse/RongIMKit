package com.android.tools;

public class Friend {
	private String id;
	private String friendID;
	private String nickname;
	private String portarait;
	private String portaraitStatus;
	private String age;
	private String height;
	private String province;
	private String city;
	private String lastTime;
	private String newNum;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getPortarait() {
		return portarait;
	}

	public void setPortarait(String portarait) {
		this.portarait = portarait;
	}

	public String getPortaraitStatus() {
		return portaraitStatus;
	}

	public void setPortaraitStatus(String portaraitStatus) {
		this.portaraitStatus = portaraitStatus;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getLastTime() {
		return lastTime;
	}

	public void setLastTime(String lastTime) {
		this.lastTime = lastTime;
	}

	public String getNewNum() {
		return newNum;
	}

	public void setNewNum(String newNum) {
		this.newNum = newNum;
	}

	public String getFriendID() {
		return friendID;
	}

	public void setFriendID(String friendID) {
		this.friendID = friendID;
	}

}
